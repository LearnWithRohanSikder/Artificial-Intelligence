	private NeuralNetwork trainNetwork() throws Exception {
		var builder = NetworkBuilderFactory.getInstance().newNetworkBuilder();
		NeuralNetwork net = builder
				.inputLayer("Input", 7)
				.hiddenLayer("Hidden1", Activation.SIGMOID, 16)
				.hiddenLayer("Hidden2", Activation.SIGMOID, 8)
				.outputLayer("Output", Activation.LINEAR, 1)
				.train(data, expected, 0.001, 0.95, 1000000, 0.000000001, Loss.MSE)
				.save("./MPGRunner.data")
				.build();
		return net;
	}

	private void testNetwork(NeuralNetwork net) throws Exception {
		for (int i = 0; i < data.length; i++) {
			long predicted = round(net.process(data[i], Output.NUMERIC));
			long actual = round(expected[i][0]);
			System.out.print(predicted + "==" + actual + "\t");
			System.out.println(actual == predicted ? "[OK]" : "[Error]");
		}
	}

